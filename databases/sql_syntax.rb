# 8.1: Intro to Databases
=begin
List of SQL Commands:
- To run SQL in the console: sqlite3 <file_name.db>
- List available commands: .help
- Exit: .exit
- View tables: .tables
- View table: SELECT * FROM <table name>;
- View specifc values in table: SELECT <col name>, <add col name> FROM <table name>;
- View specifc values in table: SELECT * FROM <table name> WHERE <boolean col name>="<true or false>";
- Insert row: INSERT INTO <table name> (column data, column data,) VALUES (value, value);
- Update row in table: UPDATE <table name> SET <col name>=<updated value> WHERE <col name>=<value>;
- Create table: CREATE TABLE table_name (
  attr_name DATATYPE,
  attr_name DATATYPE,
  attr_name DATATYPE
);
- Delete row in table: DELETE FROM <table name> WHERE <col name> = <value>;





=end